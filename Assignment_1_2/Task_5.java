//Using a while loop, display the quote one hundred times (quote variable):
//        “I will never work for pennies."
//Output each value on a new line.
//        Requirements:
//        • The program must display text on the screen.
//        • The program must display the contents of the quote variable 100 times.
//• The program must use a while loop.


package Assignment_1_2;

public class Task_5 {
    public  static  void main(String[] args){
        int n = 0;
        while (n < 100) {
            System.out.println("I will never work for pennies");
            n++;
        }
    }
}
